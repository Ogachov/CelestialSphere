    public class Info
    {
        public int RHour; // right ascension = 赤経
        public int RMinute;
        public float RSecond;
        public int DDegree; // 赤緯
        public int DMinute;
        public float DSecond;
        public float Magnitude;
        public char Spectrum;

        private const float Radius = 100.0f;

        public Info(int rHour, int rMinute, float rSecond, int dDegree, int dMinute, float dSecond,
            float magnitude, char spectrum)
        {
            RHour = rHour;
            RMinute = rMinute;
            RSecond = rSecond;
            DDegree = dDegree;
            DMinute = dMinute;
            DSecond = dSecond;
            Magnitude = magnitude;
            Spectrum = spectrum;
        }

        public Vector3 CalcPosition()
        {
            var rDegree = ((RHour * 60.0f + RMinute) * 60.0f + RSecond) * (360.0f / (24.0f * 60 * 60));
            var dDegree = DDegree + DMinute / 60.0f + DSecond / 3600.0f;

            Quaternion quat = Quaternion.Euler(-dDegree, -rDegree, 0.0f);
            var v = quat * (Vector3.forward * Radius);

            return v;
        }

        public Color Color
        {
            get
            {
                return SpectrumColor.ContainsKey(Spectrum) ? SpectrumColor[Spectrum] : Color.white;
            }
        }
    }
    
